list = ['Google', 'Runoob', 1997, 2000]
print("列表的内容为：",list)
print("第三个元素为：", list[2])
list[2] = 2001
print("修改元素操作: list[2] = 2001")
print("更新后的第三个元素为：",list[2])
print("更新后的列表为：",list)

print("*************************************************")
print("删除元素操作: del list[2]")
del list[2]
print("删除第三个元素后的列表为：",list)

