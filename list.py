print("组合与重复列表和字符串操作类似，用+和*号")
print("len([1,2,3]) 的执行结果为：",len([1,2,3]))
print("[1, 2, 3] + [4, 5, 6] 的执行结果为：",[1, 2, 3] + [4, 5, 6])
print("['Hi!'] * 4 的执行结果为：",['Hi!'] * 4)
print("3 in [1, 2, 3] 的执行结果为：",3 in [1, 2, 3])
print("for x in [1, 2, 3]: print(x, end=' ') 的执行结果为：")
for x in [1, 2, 3]: print(x, end=" ")

