import numpy as np

print('np.array([1,2,3])')
a = np.array([1,2,3])
print(a)
print()

print("np.array([[1,2],[3,4]])")
b = np.array([[1,2],[3,4]])
print(b)
print()

print("np.array([1,2,3],ndmin=3)")
c = np.array([1,2,3],ndmin=3)
print(c)
print()

print("np.array([1,2,3],dtype=complex)")
d = np.array([1,2,3],dtype=complex)
print(d)
print()
